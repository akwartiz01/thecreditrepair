                                        <ol class="margin-10">
                                            <li>
                                                <strong>Order fresh new copies of your credit reports from all 3 bureaus: Equifax, Experian and TransUnion. </strong><strong>*We will assist you with this step.</strong><br>
                                                Credit reports are constantly changing. Therefore it is important to up-to-date copies. A good rule of thumb to know is: If someone else runs your score or reports, this will hurt your score. However, <strong>if you order your own credit reports</strong> (which we will help you with) <strong>your score will not be affected. </strong>You also may want to sign up for credit monitoring to see your reports and score and track changes as they happen.

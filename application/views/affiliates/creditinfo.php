                                        <ol start="2" class="margin-10">
                                            <li>
                                                <p> <strong>Correct all inaccuracies on your Credit Reports. *We will assist you with this step.</strong><br>
                                                    Go through your credit reports very carefully. Especially look for; Late payments, charge-offs, collections or other negative items that aren't yours, Accounts listed as "settled," "paid derogatory," "paid charge-off" or anything other than "current" or "paid as agreed" if you paid on time and in full, Accounts that are still listed as unpaid that were included in a bankruptcy, Negative items older than seven years (10 in the case of bankruptcy) that should have automatically fallen off your report (you must be careful with this last one, because sometimes scores actually go down when bad items fall off your report. It's a quirk in the FICO credit-scoring software, and the potential effect of eliminating old negative items is difficult to predict in advance). Also make sure you don't have duplicate collection notices listed. For example; if you have an account that has gone to collections, the original creditor may list the debt, as well as the collection agency. Any duplicates must be removed! Make sure that your proper credit lines are posted on your Credit Reports. Often, in an effort to make you less desirable to their competitors, some creditors will not post your proper credit line. Showing less available credit can negatively impact your credit score. If you see this happening on your credit report, you have a right to complain and bring this to their attention. If you have bankruptcies that should be showing a zero balance…make sure they show a zero balance! Very often the creditor will not report a "bankruptcy charge-off" as a zero balance until it's been disputed.</p>
                                            </li>
                                            <li>
                                                <p><strong>If you have any negative marks on your credit report, negotiate with the creditor or lender to remove it. *We will assist you with this step.</strong><br>

                                                    If you are a long time customer and it's something simple like a one-time late payment, a creditor will often wipe it away to keep you as a loyal customer. Sometimes they will do this if you call and ask. However, if you have a serious negative mark (such as a long overdue bill that has gone to collections), always negotiate a payment in exchange for removal of the negative item. Always make sure you have this agreement with them in writing. Do not pay off a bill that has gone to collections unless the creditor agrees in writing that they will remove the derogatory item from your credit report. This is important; when speaking with the creditor or collection agency about a debt that has gone to collections, do not admit that the debt is yours. Admission of debt can restart the statute of limitations, and may enable the creditor to sue you. You are also less likely to be able to negotiate a letter of deletion if you admit that this debt is yours. Simply say "I'm calling about account number ________" instead of "I'm calling about my past due debt." Again, as your credit specialist, we will help you with this step.</p>

                                            </li>
                                            <li>
                                                <p><strong>Pay all credit cards and any revolving credit down to below 30% of the available credit line.</strong>
                                                    <br>
                                                    This step alone can make a huge impact on your score. The credit scoring system wants to make sure you aren't overextended, but at the same time, they want to see that you do indeed use your credit. 30% of the available credit line seems to be the magic "balance vs. credit line" ratio to have. For example; if you have a Credit Card with a $10,000 credit line, make sure that never more than $3000 (even if you pay your account off in full each month). If your balances are higher than 30% of the available credit line, pay them down. Here is another thing you can try; ask your long time reditors if they will raise your Credit Line without checking your FICO score or your Credit Report. Tell them that you're shopping for a house and you can't afford to have any hits on your credit report. Many will not but some will.
                                                </p>
                                            </li>
                                            <li>
                                                <p><strong>Do not close your old credit card accounts.</strong> <br>
                                                    Old established accounts show your history, and tell about your stability and paying habits. If you have old credit card accounts that you want to stop using, just cut up the cards or keep them in a drawer, but keep the accounts open. </p>
                                            </li>
                                            <li>
                                                <p><strong>Avoid applying for new credit. <br>
                                                    </strong>
                                                    Do not apply for any new credit! Each time you apply for new credit, your credit report gets checked. New credit cards will not help your credit score and a credit account less than one year old may hurt your credit score. Use your cards and credit as little as possible until the next credit scoring.</p>
                                            </li>
                                            <li>
                                                <p><strong>Have at least three revolving credit lines and one active (or paid) installment loan listed on your Credit Report.</strong><br>
                                                    The scoring system wants to see that you maintain a variety of credit accounts. It also wants to see that you have 3 revolving credit lines. If you do not have three active credit cards, you might want to open some (but keep in mind that if you do, you will need to wait some time before rescoring). If you have poor credit and are not approved for a typical credit card, you might want to set up a "secured credit card" account. This means that you will have to make a deposit that is equal or more than your limit, which guarantees the bank that you will repay the loan. It's an excellent way to establish credit. Examples of an installment loan would be a car loan, or it could be for furniture or a major appliance. In addition to the above, having a mortgage listed will bring your score even higher.</p>
                                            </li>
                                        </ol>

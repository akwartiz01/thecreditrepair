        $('#consult-form').submit(function(e) {
            e.preventDefault();

            // Check for existing email warning
            if (!$('#consult-email-check').hasClass('d-none')) {
                Swal.fire('Error', 'Email already exists!', 'error');
                return;
            }

            let countdown = 5;

            // Show loading Swal
            Swal.fire({
                title: 'Submitting Your Consultation Request...',
                html: `<p>Please wait while we process your request. <b id="countdown">${countdown}</b> seconds remaining.</p>
               <p>We are verifying your details and preparing the next steps.</p>`,
                allowOutsideClick: false,
                showConfirmButton: false,
                didOpen: () => {
                    const content = Swal.getHtmlContainer();
                    const countdownElem = content.querySelector('#countdown');

                    const interval = setInterval(() => {
                        countdown--;
                        countdownElem.textContent = countdown;
                        if (countdown === 0) {
                            clearInterval(interval);
                        }
                    }, 1000);

                    // Add shake animation on outside click
                    $('.swal2-container').on('click', function(event) {
                        if (!Swal.getPopup().contains(event.target)) {
                            let popup = Swal.getPopup();
                            $(popup).css('animation', 'shake 0.5s');

                            setTimeout(() => {
                                $(popup).css('animation', '');
                            }, 500);
                        }
                    });
                }
            });

            // Submit form via AJAX
            $.ajax({
                url: '<?php echo base_url('submit-consultation'); ?>',
                type: 'POST',
                data: $('#consult-form').serialize(),
                dataType: 'json',
                success: function(response) {
                    Swal.close(); // Close the loading Swal first

                    if (response.status === 'success') {
                        Swal.fire('Success!', response.message, 'success');
                        $('#consult-form')[0].reset();
                    } else {
                        Swal.fire('Oops!', response.message, 'error');
                    }
                },
                error: function() {
                    Swal.close();
                    Swal.fire('Error', 'An unexpected error occurred. Please try again later.', 'error');
                }
            });
        });

    });
</script>